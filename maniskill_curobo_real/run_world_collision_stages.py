def main(argv: Iterable[str] | None = None) -> int:
    args = parse_args(argv)
    if args.point_cloud_source == "zerograsp_reconstruction":
        unsupported = sorted(set(args.stages) - {"m3", "m4a", "m4b", "m4c"})
        if unsupported:
            raise ValueError(
                "ZeroGrasp reconstruction experiments start at M3; unsupported "
                f"stages: {unsupported}"
            )
        if not args.zerograsp_reconstruction_root:
            raise ValueError(
                "--zerograsp-reconstruction-root is required for "
                "--point-cloud-source zerograsp_reconstruction."
            )
    if "m4c" in args.stages and not args.persistent_execution:
        raise ValueError("M4-C native VoxelGrid requires --persistent-execution.")
    output_root = Path(args.output_root).expanduser().resolve()
    output_root.mkdir(parents=True, exist_ok=True)
    candidate_root = Path(args.reuse_candidate_root).expanduser().resolve()
    seeds = list(range(int(args.seed_start), int(args.seed_end) + 1))
    records_by_stage = load_existing_records(output_root) if args.reuse_existing else {}
    execution_runner = (
        PersistentRealExecutionRunner(repo_root=Path(__file__).resolve().parents[1])
        if args.persistent_execution
        else None
    )

    try:
        for stage in args.stages:
            stage_label = stage_label_for(args, stage)
            print(f"=== {stage_label} seeds {seeds[0]}-{seeds[-1]} ===", flush=True)
            stage_records = records_by_stage.setdefault(stage, {})
            if stage == "m1":
                write_global_m1_scene(output_root / stage_label / "scene")
            for index, seed in enumerate(seeds, start=1):
                print(f"[{stage_label}] seed {seed} ({index}/{len(seeds)})", flush=True)
                existing = stage_records.get(seed)
                record = run_stage_seed(
                    args=args,
                    stage=stage,
                    output_root=output_root,
                    candidate_root=candidate_root,
                    seed=seed,
                    existing=existing,
                    execution_runner=execution_runner,
                )
                stage_records[seed] = record
                write_results(output_root, args, seeds, records_by_stage)
                print(
                    f"  status={record.get('status')} outcome={record.get('outcome')} "
                    f"lift={record.get('object_lift_success')}",
                    flush=True,
                )
    finally:
        if execution_runner is not None:
            execution_runner.close()

    summary = write_results(output_root, args, seeds, records_by_stage)
    print(json.dumps(summary["counts"], ensure_ascii=False, indent=2), flush=True)
    return 0


def run_stage_seed(
    *,
    args: argparse.Namespace,
    stage: str,
    output_root: Path,
    candidate_root: Path,
    seed: int,
    existing: dict[str, Any] | None,
    execution_runner: "PersistentRealExecutionRunner | None" = None,
) -> dict[str, Any]:
    run_started = time.time()
    stage_label = stage_label_for(args, stage)
    run_dir = output_root / stage_label / f"seed{seed:03d}"
    manifest_path = run_dir / "run_manifest.json"
    if args.reuse_existing and manifest_path.is_file():
        return summarize_run(stage=stage, seed=seed, run_dir=run_dir, reused=True)

    zg_output_dir = find_candidate_output(candidate_root, seed)
    if zg_output_dir is None and args.generate_missing_candidates:
        zg_output_dir = generate_candidate_for_seed(
            args=args,
            seed=seed,
            candidate_dir=output_root / "candidates" / f"seed{seed:03d}" / "setup",
        )
    if zg_output_dir is None:
        return {
            "stage": stage,
            "seed": seed,
            "status": "missing_zerograsp_candidate",
            "candidate_root": str(candidate_root),
        }

    scene_model: Path | None = None
    scene_source = "maniskill"
    scene_build: dict[str, Any] | None = None
    if stage == "m1":
        scene_source = "fixed"
        scene_model = output_root / stage_label / "scene" / "curobo_scene.yml"
    elif stage in {"m2", "m3", "m4a", "m4b", "m4c"}:
        scene_source = "fixed"
        scene_name = "curobo_scene_voxel.npz" if stage == "m4c" else "curobo_scene.yml"
        scene_model = run_dir / "real_scene" / scene_name
        scene_env = execution_runner.acquire_env(args) if execution_runner is not None else None
        scene_build = build_depth_scene_for_seed(
            args=args,
            seed=seed,
            stage=stage,
            scene_path=scene_model,
            env=scene_env,
        )

    command = execute_command(
        args=args,
        seed=seed,
        zg_output_dir=zg_output_dir,
        run_dir=run_dir,
        scene_source=scene_source,
        scene_model=scene_model,
    )
    if execution_runner is None:
        result = run_command(
            command,
            cwd=Path(__file__).resolve().parents[1],
            logs_dir=run_dir / "logs",
            name="execute",
        )
    else:
        result = execution_runner.run(
            command,
            logs_dir=run_dir / "logs",
            name="execute",
        )
    summary = summarize_run(stage=stage, seed=seed, run_dir=run_dir, command=result)
    summary["scene_build"] = scene_build
    summary["total_runtime_sec"] = float(time.time() - run_started)
    return summary


def generate_candidate_for_seed(
    *,
    args: argparse.Namespace,
    seed: int,
    candidate_dir: Path,
) -> Path | None:
    input_dir = candidate_dir / "zg_input"
    output_dir = candidate_dir / "zg_output"
    logs_dir = candidate_dir / "logs"
    candidate_path = output_dir / "recommended_grasp_top1.json"
    if candidate_path.is_file():
        return output_dir

    export_result = run_command(
        export_command(args=args, seed=seed, input_dir=input_dir),
        cwd=Path(__file__).resolve().parents[1],
        logs_dir=logs_dir,
        name="export_input",
    )
    if export_result["exit_code"] != 0:
        return None
    zg_result = run_command(
        zerograsp_command(args=args, seed=seed, input_dir=input_dir, output_dir=output_dir),
        cwd=Path(__file__).resolve().parents[1],
        logs_dir=logs_dir,
        name="zerograsp",
    )
    if zg_result["exit_code"] != 0 or not candidate_path.is_file():
        return None
    return output_dir


