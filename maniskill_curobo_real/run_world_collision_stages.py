def build_depth_scene_for_seed(
    *,
    args: argparse.Namespace,
    seed: int,
    stage: str,
    scene_path: Path,
    env: Any | None = None,
) -> dict[str, Any]:
    build_started = time.time()
    from maniskill_codex.zerograsp_inputs import (
        collect_mask_actor_records,
        extract_zerograsp_input,
    )
    from maniskill_curobo.scripts import execute_curobo_pick as execute

    env_args = argparse.Namespace(
        env_id=args.env_id,
        seed=seed,
        camera=args.camera,
        width=args.width,
        height=args.height,
        render_width=args.render_width,
        render_height=args.render_height,
        camera_eye=args.camera_eye,
        camera_target=args.camera_target,
        gripper_open=1.0,
    )
    owns_env = env is None
    if env is None:
        env = build_real_benchmark_env(env_args)
    try:
        obs, _ = env.reset(seed=seed)
        if args.settle_before_export_steps > 0:
            obs, _ = execute.settle_environment(
                env,
                steps=int(args.settle_before_export_steps),
                gripper=1.0,
            )
        camera_model = execute.camera_model_matrix(env, args.camera)
        world_from_base = execute.robot_base_matrix(env)
        if args.point_cloud_source == "zerograsp_reconstruction":
            reconstruction_dirs = find_reconstruction_dirs(
                Path(args.zerograsp_reconstruction_root).expanduser().resolve(),
                seed,
            )
            if reconstruction_dirs is None:
                raise FileNotFoundError(
                    f"Missing ZeroGrasp reconstruction for seed={seed} under "
                    f"{args.zerograsp_reconstruction_root}"
                )
            input_dir, output_dir = reconstruction_dirs
            from maniskill_curobo_real.zerograsp_reconstruction import (
                load_zerograsp_reconstructed_instances,
            )

            instances = load_zerograsp_reconstructed_instances(
                input_dir=input_dir,
                output_dir=output_dir,
                camera_model_matrix=camera_model,
                world_from_base_matrix=world_from_base,
            )
            if stage == "m3":
                result = build_zerograsp_instance_cuboid_scene(
                    instances=instances,
                    min_instance_points=int(args.m3_min_instance_points),
                    instance_padding=float(args.m3_instance_padding),
                    min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                    max_obstacles=int(args.m3_max_obstacles),
                )
            elif stage == "m4a":
                result = build_zerograsp_instance_obb_scene(
                    instances=instances,
                    min_instance_points=int(args.m4_min_instance_points),
                    instance_padding=float(args.m4_instance_padding),
                    min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                    max_obstacles=int(args.m4_max_obstacles),
                )
            elif stage == "m4b":
                result = build_zerograsp_instance_mesh_scene(
                    instances=instances,
                    min_instance_points=int(args.m4_min_instance_points),
                    max_obstacles=int(args.m4_max_obstacles),
                    max_points_per_instance=int(args.m4_mesh_max_points),
                    fallback_padding=float(args.m4_instance_padding),
                    min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                )
            elif stage == "m4c":
                result = build_zerograsp_instance_voxel_esdf_scene(
                    instances=instances,
                    min_instance_points=int(args.m4_min_instance_points),
                    max_obstacles=int(args.m4_max_obstacles),
                    max_points_per_instance=int(args.m4_voxel_max_points),
                    voxel_size=float(args.m4_voxel_size),
                    dilation_voxels=int(args.m4_voxel_dilation),
                )
            else:
                raise ValueError(
                    f"ZeroGrasp reconstruction is unsupported for stage={stage!r}."
                )
            if args.m3_debug_views:
                write_zerograsp_debug_views(
                    output_dir=scene_path.parent / "debug_views",
                    input_dir=input_dir,
                    instances=instances,
                    camera_model=camera_model,
                    world_from_base=world_from_base,
                    min_instance_points=int(
                        args.m3_min_instance_points
                        if stage == "m3"
                        else args.m4_min_instance_points
                    ),
                    instance_padding=float(
                        args.m3_instance_padding
                        if stage == "m3"
                        else args.m4_instance_padding
                    ),
                    min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                    max_points=int(args.m3_debug_max_points),
                )
        else:
            bundle = extract_zerograsp_input(
                obs,
                env,
                args.camera,
                mask_mode=args.mask_mode,
            )
            if stage == "m2":
                result = build_mask_exclusion_scene(
                    depth_m=bundle.depth,
                    mask=bundle.mask,
                    camera_matrix=bundle.camera_matrix,
                    camera_model_matrix=camera_model,
                    world_from_base_matrix=world_from_base,
                )
            elif stage in {"m3", "m4a", "m4b", "m4c"}:
                segmentation = raw_segmentation(obs, args.camera)
                all_object_records = collect_mask_actor_records(env, "all-objects")
                target_records = collect_mask_actor_records(env, "task-target")
                common = {
                    "depth_m": bundle.depth,
                    "mask": bundle.mask,
                    "segmentation": segmentation,
                    "object_records": all_object_records,
                    "target_records": target_records,
                    "camera_matrix": bundle.camera_matrix,
                    "camera_model_matrix": camera_model,
                    "world_from_base_matrix": world_from_base,
                }
                if stage == "m3":
                    result = build_oracle_instance_cuboid_scene(
                        **common,
                        min_instance_points=int(args.m3_min_instance_points),
                        instance_padding=float(args.m3_instance_padding),
                        min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                        max_obstacles=int(args.m3_max_obstacles),
                    )
                elif stage == "m4a":
                    result = build_oracle_instance_obb_scene(
                        **common,
                        min_instance_points=int(args.m4_min_instance_points),
                        instance_padding=float(args.m4_instance_padding),
                        min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                        max_obstacles=int(args.m4_max_obstacles),
                    )
                elif stage == "m4b":
                    result = build_oracle_instance_mesh_scene(
                        **common,
                        min_instance_points=int(args.m4_min_instance_points),
                        max_obstacles=int(args.m4_max_obstacles),
                        max_points_per_instance=int(args.m4_mesh_max_points),
                        fallback_padding=float(args.m4_instance_padding),
                        min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                    )
                else:
                    result = build_oracle_instance_voxel_esdf_scene(
                        **common,
                        min_instance_points=int(args.m4_min_instance_points),
                        max_obstacles=int(args.m4_max_obstacles),
                        max_points_per_instance=int(args.m4_voxel_max_points),
                        voxel_size=float(args.m4_voxel_size),
                        dilation_voxels=int(args.m4_voxel_dilation),
                    )
                if args.m3_debug_views:
                    write_m3_debug_views(
                        output_dir=scene_path.parent / "debug_views",
                        bundle=bundle,
                        segmentation=segmentation,
                        object_records=all_object_records,
                        target_records=target_records,
                        camera_model=camera_model,
                        world_from_base=world_from_base,
                        min_instance_points=int(
                            args.m3_min_instance_points
                            if stage == "m3"
                            else args.m4_min_instance_points
                        ),
                        instance_padding=float(
                            args.m3_instance_padding
                            if stage == "m3"
                            else args.m4_instance_padding
                        ),
                        min_cuboid_dimension=float(args.scene_min_cuboid_dimension),
                        max_points=int(args.m3_debug_max_points),
                    )
            else:
                raise ValueError(f"Depth scene build is unsupported for stage={stage!r}.")
        valid_points = int(result.metadata.get("valid_points", 0))
        minimum_valid_points = (
            100
            if args.point_cloud_source == "zerograsp_reconstruction"
            else 1000
        )
        if valid_points < minimum_valid_points:
            raise RuntimeError(
                f"Invalid collision scene for seed={seed}, stage={stage}: "
                f"valid_points={valid_points}, required={minimum_valid_points}."
            )
        metadata_path = scene_path.with_name("curobo_scene_metadata.json")
        if stage == "m4c":
            write_voxel_scene_result(result, scene_path, metadata_path=metadata_path)
        else:
            write_scene_result(result, scene_path, metadata_path=metadata_path)
        return {
            "scene_model": str(scene_path),
            "metadata": str(metadata_path),
            "runtime_sec": float(time.time() - build_started),
            "summary": {
                "source": result.metadata.get("source"),
                "n_obstacles": result.metadata.get("n_obstacles"),
                "target_points": result.metadata.get("target_points"),
                "non_target_points": result.metadata.get("non_target_points"),
                "n_pointcloud_obstacles": result.metadata.get("n_pointcloud_obstacles"),
            },
        }
    finally:
        if owns_env:
            env.close()


