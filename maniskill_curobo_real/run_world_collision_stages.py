def build_real_benchmark_env(args: argparse.Namespace) -> Any:
    """Build a sensor env without mixing minimal and default shader formats."""

    import gymnasium as gym
    import mani_skill.envs  # noqa: F401
    from mani_skill.render import PREBUILT_SHADER_CONFIGS, set_shader_pack
    from mani_skill.utils import sapien_utils

    # Importing the execution stack can leave SAPIEN's global picture formats
    # on the default shader while the sensor config says minimal. Reset both
    # the shader directory and integer PositionSegmentation format explicitly.
    set_shader_pack(PREBUILT_SHADER_CONFIGS["minimal"])
    sensor_configs: dict[str, Any] = {
        "width": args.width,
        "height": args.height,
        "shader_pack": "minimal",
        args.camera: {
            "pose": sapien_utils.look_at(list(args.camera_eye), list(args.camera_target)),
            "shader_pack": "minimal",
        },
    }
    return gym.make(
        args.env_id,
        render_mode="rgb_array",
        control_mode="pd_joint_pos",
        robot_uids="panda",
        obs_mode="sensor_data",
        max_episode_steps=1000,
        sensor_configs=sensor_configs,
        human_render_camera_configs={
            "width": args.render_width,
            "height": args.render_height,
            "shader_pack": "minimal",
        },
    )


def scene_model_signature(scene_model: Any) -> str:
    if isinstance(scene_model, dict):
        return json.dumps(scene_model, sort_keys=True)
    if scene_model is None:
        return "none"
    path = Path(scene_model).expanduser()
    if path.is_file():
        if path.suffix == ".npz":
            stat = path.stat()
            return f"{path.resolve()}:{stat.st_size}:{stat.st_mtime_ns}"
        return path.read_text(encoding="utf-8").strip()
    return str(scene_model)


def load_planner_scene_model(scene_model: Any) -> Any:
    if scene_model is None or isinstance(scene_model, dict):
        return scene_model
    path = Path(scene_model).expanduser()
    if path.is_file() and path.suffix in {".yml", ".yaml", ".json"}:
        from curobo.scene import Scene

        return Scene.create(json.loads(path.read_text(encoding="utf-8")))
    if path.suffix != ".npz":
        return scene_model

    import numpy as np
    import torch
    from curobo.scene import Cuboid, Scene, VoxelGrid

    with np.load(path) as payload:
        feature = torch.as_tensor(
            payload["feature_tensor"],
            dtype=torch.float16,
            device="cuda:0",
        )
        table = Cuboid(
            name="real_table_static",
            pose=payload["table_pose"].astype(float).tolist(),
            dims=payload["table_dims"].astype(float).tolist(),
        )
        voxel = VoxelGrid(
            name="m4c_workspace_esdf",
            pose=[
                *payload["voxel_center"].astype(float).tolist(),
                1.0,
                0.0,
                0.0,
                0.0,
            ],
            dims=payload["voxel_dims"].astype(float).tolist(),
            voxel_size=float(payload["voxel_size"]),
            feature_tensor=feature,
            feature_dtype=torch.float16,
        )
    return Scene(cuboid=[table], voxel=[voxel])


def planner_scene_family(scene_model: Any) -> str:
    if scene_model is None:
        return "none"
    if isinstance(scene_model, (str, Path, dict)):
        return "serialized"
    if getattr(scene_model, "voxel", None):
        return "voxel"
    if getattr(scene_model, "mesh", None):
        return "mesh"
    return "cuboid"


def build_cached_planner(args: argparse.Namespace, scene_model: Any) -> Any:
    from curobo.motion_planner import MotionPlanner, MotionPlannerCfg

    collision_cache: dict[str, Any] = {"cuboid": 64}
    if getattr(scene_model, "mesh", None):
        collision_cache["mesh"] = 64
    if getattr(scene_model, "voxel", None):
        voxel = scene_model.voxel[0]
        collision_cache["voxel"] = {
            "layers": 1,
            "dims": list(voxel.dims),
            "voxel_size": float(voxel.voxel_size),
        }
    config = MotionPlannerCfg.create(
        robot=args.robot_config,
        scene_model=scene_model,
        collision_cache=collision_cache,
    )
    planner = MotionPlanner(config)
    planner.warmup(
        enable_graph=True,
        num_warmup_iterations=args.warmup_iterations,
    )
    print(f"curobo_scene_model={scene_model}")
    print(f"curobo_joint_names={planner.joint_names}")
    print(f"curobo_tool_frames={planner.tool_frames}")
    return planner


def in_process_execute_argv(command: list[str]) -> list[str]:
    if len(command) < 3 or command[1:3] != [
        "-m",
        "maniskill_curobo.scripts.execute_curobo_pick",
    ]:
        raise ValueError(f"Unsupported persistent execution command: {command}")
    return command[3:]


def run_command(
    command: list[str],
    *,
    cwd: Path,
    logs_dir: Path,
    name: str,
) -> dict[str, Any]:
    logs_dir.mkdir(parents=True, exist_ok=True)
    stdout_path = logs_dir / f"{name}.stdout.log"
    stderr_path = logs_dir / f"{name}.stderr.log"
    command_path = logs_dir / f"{name}.command.sh"
    command_path.write_text("PYTHONPATH=. " + shlex.join(command) + "\n", encoding="utf-8")
    started = time.time()
    with stdout_path.open("w", encoding="utf-8") as stdout, stderr_path.open(
        "w", encoding="utf-8"
    ) as stderr:
        proc = subprocess.run(
            command,
            cwd=str(cwd),
            stdout=stdout,
            stderr=stderr,
            text=True,
            env=env_with_pythonpath(),
        )
    return {
        "exit_code": int(proc.returncode),
        "runtime_sec": float(time.time() - started),
        "stdout_log": str(stdout_path),
        "stderr_log": str(stderr_path),
        "command_path": str(command_path),
    }


def summarize_run(
    *,
    stage: str,
    seed: int,
    run_dir: Path,
    command: dict[str, Any] | None = None,
    reused: bool = False,
) -> dict[str, Any]:
    manifest_path = run_dir / "run_manifest.json"
    if not manifest_path.is_file():
        return {
            "stage": stage,
            "seed": seed,
            "status": "missing_run_manifest",
            "outcome": "missing_run_manifest",
            "object_lift_success": False,
            "run_dir": str(run_dir),
            "command": command,
            "reused": reused,
        }
    manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    metrics = manifest.get("object_lift_metrics") or {}
    grasp = manifest.get("grasp") or {}
    return {
        "stage": stage,
        "seed": seed,
        "status": "complete",
        "outcome": manifest_outcome(manifest),
        "object_lift_success": bool(metrics.get("object_lift_success")),
        "height_delta_m": metrics.get("height_delta_m"),
        "max_height_delta_m": metrics.get("max_height_delta_m"),
        "final_object_tcp_distance_m": metrics.get("final_object_tcp_distance_m"),
        "failure_reason": manifest.get("failure_reason")
        or metrics.get("failure_reason")
        or "",
        "depth_m": grasp.get("depth_m"),
        "depth_offset": grasp.get("grasp_depth_offset"),
        "candidate_selection": grasp.get("candidate_selection"),
        "curobo_scene": manifest.get("curobo_scene"),
        "run_dir": str(run_dir),
        "manifest_path": str(manifest_path),
        "video_path": manifest.get("video_saved"),
        "command": command,
        "reused": reused,
    }


def manifest_outcome(manifest: dict[str, Any]) -> str:
    metrics = manifest.get("object_lift_metrics") or {}
    if metrics.get("object_lift_success"):
        return "success"
    reason = str(manifest.get("failure_reason") or "")
    if "candidate selection failed" in reason:
        return "planning_failed_candidate_selection"
    if "stage=pre" in reason:
        return "planning_failed_pre"
    if "stage=grasp" in reason:
        return "planning_failed_grasp"
    if "stage=lift" in reason:
        return "planning_failed_lift"
    if reason:
        return "execution_failed"
    return str(metrics.get("failure_reason") or "failed")


