class NonClosingEnvProxy:
    """Delegate to a Gym environment while keeping it alive between episodes."""

    def __init__(self, env: Any):
        object.__setattr__(self, "_env", env)

    def __getattr__(self, name: str) -> Any:
        return getattr(self._env, name)

    def __setattr__(self, name: str, value: Any) -> None:
        setattr(self._env, name, value)

    def close(self) -> None:
        return None


class PersistentRealExecutionRunner:
    """Run execute_curobo_pick in-process with cached ManiSkill resources.

    M3 uses per-seed fixed scene files, so the cuRobo planner is rebuilt when
    the scene signature changes. The optimization here avoids repeatedly
    launching Python and rebuilding the ManiSkill renderer/camera stack.
    """

    def __init__(
        self,
        *,
        repo_root: Path,
        execute_module: Any | None = None,
    ):
        if execute_module is None:
            from maniskill_curobo.scripts import execute_curobo_pick as execute_module

        self.repo_root = Path(repo_root).resolve()
        self.execute_module = execute_module
        self._original_build_env: Callable[..., Any] = execute_module.build_env
        self._original_build_planner: Callable[..., Any] = execute_module.build_planner
        self._env: Any | None = None
        self._env_proxy: NonClosingEnvProxy | None = None
        self._env_signature: tuple[Any, ...] | None = None
        self._planner: Any | None = None
        self._scene_signature: str | None = None
        self._scene_family: str | None = None
        self._episode_count = 0
        self._planner_rebuild_count = 0
        self._last_planner_reused = False
        execute_module.build_env = self._build_env
        execute_module.build_planner = self._build_planner

    def _build_env(self, args: argparse.Namespace) -> NonClosingEnvProxy:
        signature = environment_signature(args)
        if self._env is None:
            self._env = build_real_benchmark_env(args)
            self._env_proxy = NonClosingEnvProxy(self._env)
            self._env_signature = signature
        elif signature != self._env_signature:
            raise RuntimeError(
                "Persistent M3 worker environment configuration changed: "
                f"expected={self._env_signature}, received={signature}"
            )
        assert self._env_proxy is not None
        return self._env_proxy

    def acquire_env(self, args: argparse.Namespace) -> NonClosingEnvProxy:
        """Return the same persistent env used by execute_curobo_pick."""

        return self._build_env(args)

    def _build_planner(
        self,
        args: argparse.Namespace,
        scene_model: Any = None,
    ) -> Any:
        signature = scene_model_signature(scene_model)
        planner_scene_model = load_planner_scene_model(scene_model)
        family = planner_scene_family(planner_scene_model)
        if self._planner is not None and signature == self._scene_signature:
            reset_seed = getattr(self._planner, "reset_seed", None)
            if callable(reset_seed):
                reset_seed()
            self._last_planner_reused = True
            return self._planner
        if self._planner is not None and family == self._scene_family:
            self._planner.clear_scene_cache()
            self._planner.update_world(planner_scene_model)
            reset_seed = getattr(self._planner, "reset_seed", None)
            if callable(reset_seed):
                reset_seed()
            self._scene_signature = signature
            self._last_planner_reused = True
            return self._planner

        self._destroy_planner()
        self._planner = build_cached_planner(args, planner_scene_model)
        self._scene_signature = signature
        self._scene_family = family
        self._planner_rebuild_count += 1
        self._last_planner_reused = False
        return self._planner

    def _destroy_planner(self) -> None:
        if self._planner is None:
            return
        destroy = getattr(self._planner, "destroy", None)
        if callable(destroy):
            destroy()
        self._planner = None
        self._scene_signature = None
        self._scene_family = None
        gc.collect()
        try:
            import torch

            if torch.cuda.is_available():
                torch.cuda.empty_cache()
        except Exception:
            pass

    def run(
        self,
        command: list[str],
        *,
        logs_dir: Path,
        name: str,
    ) -> dict[str, Any]:
        logs_dir.mkdir(parents=True, exist_ok=True)
        stdout_path = logs_dir / f"{name}.stdout.log"
        stderr_path = logs_dir / f"{name}.stderr.log"
        command_path = logs_dir / f"{name}.command.sh"
        command_path.write_text(
            "PYTHONPATH=. " + shlex.join(command) + "\n",
            encoding="utf-8",
        )
        argv = in_process_execute_argv(command)
        env_was_initialized = self._env is not None
        rebuild_count_before = self._planner_rebuild_count
        started = time.time()
        exit_code = 0
        with stdout_path.open("w", encoding="utf-8") as stdout, stderr_path.open(
            "w", encoding="utf-8"
        ) as stderr:
            try:
                with contextlib.redirect_stdout(stdout), contextlib.redirect_stderr(stderr):
                    result = self.execute_module.main(argv)
                exit_code = int(result or 0)
            except Exception:
                exit_code = 1
                traceback.print_exc(file=stderr)
        self._episode_count += 1
        return {
            "exit_code": exit_code,
            "runtime_sec": float(time.time() - started),
            "stdout_log": str(stdout_path),
            "stderr_log": str(stderr_path),
            "command_path": str(command_path),
            "persistent_execution": True,
            "episode_index": self._episode_count,
            "environment_reused": env_was_initialized,
            "planner_reused": bool(self._last_planner_reused),
            "planner_rebuilt": self._planner_rebuild_count > rebuild_count_before,
            "planner_rebuild_count": int(self._planner_rebuild_count),
        }

    def close(self) -> None:
        self.execute_module.build_env = self._original_build_env
        self.execute_module.build_planner = self._original_build_planner
        self._destroy_planner()
        if self._env is not None:
            self._env.close()
            self._env = None
            self._env_proxy = None
            self._env_signature = None


