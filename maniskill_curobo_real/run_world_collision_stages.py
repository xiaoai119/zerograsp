def write_zerograsp_debug_views(
    *,
    output_dir: Path,
    input_dir: Path,
    instances: list[Any],
    camera_model: Any,
    world_from_base: Any,
    min_instance_points: int,
    instance_padding: float,
    min_cuboid_dimension: float,
    max_points: int,
) -> None:
    from maniskill_curobo_real.visualize_m3_pointcloud import (
        save_input_images,
        visualize_oracle_instances,
    )

    output_dir.mkdir(parents=True, exist_ok=True)
    with np.load(input_dir / "rgbd.npz") as payload:
        rgb = payload["rgb"]
        mask = payload["mask"]
        camera_matrix = payload["cam_K"]
    save_input_images(output_dir, rgb, mask)

    clouds = []
    segmentations = []
    target_flags = []
    object_records = []
    target_records = []
    for instance in instances:
        points = instance.points_base
        if points.shape[0] == 0:
            continue
        segmentation_id = (
            int(instance.segmentation_id)
            if instance.segmentation_id is not None
            else int(instance.label)
        )
        clouds.append(points)
        segmentations.append(
            np.full(points.shape[0], segmentation_id, dtype="int32")
        )
        target_flags.append(
            np.full(
                points.shape[0],
                bool(instance.is_task_target),
                dtype=bool,
            )
        )
        record = {
            "segmentation_id": segmentation_id,
            "actor_name": instance.actor_name,
        }
        object_records.append(record)
        if instance.is_task_target:
            target_records.append(record)
    if not clouds:
        return
    cloud = np.concatenate(clouds, axis=0)
    segmentation = np.concatenate(segmentations, axis=0)
    target_mask = np.concatenate(target_flags, axis=0)
    visualize_oracle_instances(
        output_dir=output_dir / "zerograsp_instances",
        cloud=cloud,
        valid=np.ones(cloud.shape[0], dtype=bool),
        target_mask=target_mask,
        segmentation=segmentation,
        object_records=object_records,
        target_records=target_records,
        min_instance_points=int(min_instance_points),
        instance_padding=float(instance_padding),
        min_cuboid_dimension=float(min_cuboid_dimension),
        max_points=int(max_points),
        rgb=rgb,
        camera_matrix=camera_matrix,
        camera_model_matrix=camera_model,
        world_from_base_matrix=world_from_base,
        title="ZeroGrasp reconstructed instance point clouds",
        overlay_title="ZeroGrasp reconstructions projected on RGB",
    )


